package com.intelitune.nwms.service;

import java.util.List;

import com.intelitune.nwms.model.UnitPackageState;
/**
 * 
 * @author Louis
 *
 */
public interface UnitPackageStateService {
	/**
	 * 
	 * @param UnitPackage unitPackageState
	 * @return void
	 * 保存UnitPackageState
	 */
	void saveUnitPackageState(UnitPackageState unitPackageState);
	/**
	 * 
	 * @param UnitPackage unitPackageState
	 * @return void
	 * 更新UnitPackageState
	 */
	void updateUnitPackageState(UnitPackageState unitPackageState);
	/**
	 * 
	 * @param String code
	 * @return UnitPackageState
	 * 根据传入的UnitPackageState的code查询相应的UnitPackageState
	 */
	UnitPackageState getUnitPackageState(String code);
	/**
	 * 
	 * @return List<UnitPackageState>
	 * 查询所有的UnitPackageState
	 */
	List<UnitPackageState> findAll();
	/**
	 * 
	 * @param String id
	 * @return UnitPackageState
	 * 根据传入的UnitPackageState的Id查询相应的UnitPackageState
	 */
	UnitPackageState findUnitPackageState(String id);
}
